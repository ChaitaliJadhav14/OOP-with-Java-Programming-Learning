class DemoDoWhileLoop{
	public static void main(String[] args){
		int choice;
		choice=1;
		switch(choice){
			case 1:
				System.out.println("Register");
				break;
			case 2:
				System.out.println("Find");
				break;
			case 3:
				System.out.println("FindAll");
				break;
			case 4:
				System.out.println("Delete");
				break;
			case 5:
				System.out.println("Update");
				break;
		}
	}

}