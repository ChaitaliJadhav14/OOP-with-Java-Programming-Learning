class DemoVariable{
public static void main(String[] args){
	int rollNumber = 10;
	System.out.println(rollNumber);
	byte number = 2;
	System.out.println(number);
	long longNumber = 60;
	System.out.println(longNumber);
	float marks = 60.89f;
	System.out.println(marks);
	char ch = 'Y';
	System.out.println(ch);

}
}